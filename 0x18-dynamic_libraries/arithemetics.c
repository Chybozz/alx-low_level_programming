#include <stdio.h>

/**
 * add - addition of two numbers
 * @a: first number
 * @b: second number
 *
 * Return: final answer
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subtract two numbers
 * @a: first number
 * @b: second number
 *
 * Return: final answer
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * div - divide two numbers
 * @a: first number
 * @b: second number
 *
 * Return: final answer
 */
int div(int a, int b)
{
	return (a / b);
}
/**
 * mul - multiply two numbers
 * @a: first number
 * @b: second number
 *
 * Return: final answer
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * mod - mod two numbers
 * @a: first number
 * @b: second number
 *
 * Return: final answer
 */
int mod(int a, int b)
{
	return (a % b);
}
